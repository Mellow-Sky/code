#include<iostream>
#include<cmath>
using namespace std;

int su(int x)
{
	int i;
	int l = 1;
	for (i = 2; i <= sqrt(x); i++) {
		if (x % i == 0) {
			l = 0;
			break;
		}
	}
	return l;
}
int main()
{
	int n,i,j,k;
	cin >> n;
	for ( i = 4; i <= n; i+=2)
	{
		for (j = 2; j < n / 2; j++) {
			k = i - j;
			if (su(j) == 0 || su(k) == 0) { 
				continue; }
			if (i == k + j) {
				cout << i << "=" << j << "+" << k << endl;
				break;
			}
		}
	}
	return 0;
}